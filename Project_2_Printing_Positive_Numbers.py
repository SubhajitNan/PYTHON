#Python program to print positive Numbers in a Range
 
# list of numbers
list1 = [12, -7, 5, 64, -14]
 
# iterating each number in the first list
for num in list1:
     
    # checking condition
    if num >= 0:
       print(num, end = " ") 
    
        
